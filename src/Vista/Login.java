    private void validar(){
         principal.setEnabled(false);
         if(jTuser.getText().length() > 0 && jPFpass.getPassword().length >0) {
             SwingWorker sw;
             sw = new SwingWorker(){
              @Override
              protected Object doInBackground() throws Exception {
                  Usuario usuario = new Usuario();
                  Usuario usuarioAux= null;
                  usuario.setUser(jTuser.getText());
                  char[] clave= jPFpass.getPassword();
                  String pass= new String (clave);
                  usuario.setPass(pass);
                  usuarioAux = controladorLogin.validar(usuario);
                  if(usuarioAux != null){
                      Principal.usuario = usuarioAux;
                      principal.setEnabled(true);
                      dispose();
                   
                  }else {
                      principal.setEnabled(true);
                      JOptionPane.showMessageDialog(null, "Verifique usuario/contrase√±a", "Advertencia", JOptionPane.INFORMATION_MESSAGE);
                      limpiar();
                  }
                  return null;
                }
                  
              };
             sw.execute();
                 
             }else {
                JOptionPane.showMessageDialog(this, "Falta ingresar usuario/contrase√±a", "Advertencia", JOptionPane.INFORMATION_MESSAGE);
                  }
             }
         private void limpiar(){
             jTuser.setText("");
             jPFpass.setText("");
         }
         
         private class Validar extends SwingWorker<Boolean, JInternalFrame>{

        public Validar() {
        }

        @Override
        protected void done() {
            super.done(); 
            principal.setEnabled(true);
            dispose();
        }

        @Override
        protected Boolean doInBackground() throws Exception {
            
            Usuario usuario = new Usuario();
            Usuario usuarioAux = null;
            usuario.setUser(jTuser.getText());
            char[] clave= jPFpass.getPassword();
                  String pass= new String (clave);
                  usuario.setPass(pass);
                  usuarioAux = controladorLogin.validar(usuario);
                  if(usuarioAux != null){
                      Principal.usuario = usuarioAux;
                     return true;
                   
                  }else {        
                      limpiar();
                      return false;
                  }
                 
              }
             
            }
         
        
